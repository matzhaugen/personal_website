<h1> Pregnancy </h1>

<h2>Ultrasound and pregnancy</h2>
<ul>
<li>
<a href="https://sci-hub.st/10.1016/0140-6736(90)91941-3">A 1990 RCT</a> gave 4691 women ultrasound and 4619 no ultrasound. They experienced 20 miscarriages (*) and 11 elective abortions (due to diagnosed birth defects), whereas zero of either occurred in the control group. Additionally, it was determined that of the 250 placenta previas diagnosed by ultrasound (a key reason for prenatal ultrasounds), only 4 were present at birth.
Note: placenta previa typically resolves later in the pregnancy.
(*) means the difference was significant (p &lt; 0.05).
</li>
<li>
    <a href="https://sci-hub.st/10.1056/nejm199309163291201">A 1993 RCT</a> Conclusions: Screening ultrasonography did not improve perinatal outcome as compared with the selective use of ultrasonography on the basis of clinician judgment.
</li>
<li>
    <a href="https://sci-hub.st/10.1136/bmj.307.6895.13">Schmidt et al. (1993)</a> Found no benefit other than checking for malformations.
</li>
<li>
    <a href="https://sci-hub.st/10.1002/pd.1970140908">Bernascheck et al. (1994)</a> Found that only about 26% of all malformations were detected by ultrasound.
</li>
</ul>
