<p>
A paper called "Future Climate Emulations Using Quantile Regressions on Large Ensembles" that projects the current climate into the future with code available <a href="https://github.com/matzhaugen/future-climate-emulations-analysis">here</a>.
</p>
<li>
<p>
Research on extreme event characterization, modeling and forecasting using geological data like climate models and observations. For example, are days of extreme temperatures getting more frequent? Or is the annual temperature profile more variable with the current post-industrial climate forcings compared to the pre-industrial era?  To answer these questions I look at climate model output, e.g. Global Circulation Models, using different starting conditions and see how their behavior changes statistically. 
</p>

