<h1> Tax Law </h1>

<h2>Everyone doesn’t mean everyone</h2>
<p><a href="https://www.law.cornell.edu/supremecourt/text/213/347">American Banana Co. v. United Fruit Co., 213 U.S. 347, 357 (1909)</a>: 
    <i>“Words having universal scope, such as ‘every contract in restraint of trade,’ ‘every person who shall monopolize,’ 
        etc., will be taken, as a matter of course, to mean only everyone subject to such legislation, 
        not all that the legislator subsequently may be able to catch.”</i> 
        <br>See also <a href="https://www.law.cornell.edu/supremecourt/text/336/281">Foley Bros. v. Filardo, 336 U.S. 281 (1949)</a>; and <a href="https://www.law.cornell.edu/supremecourt/text/344/280">Steele v. Bulova Watch Co., 344 U.S. 280 (1952)</a>.
</p>
